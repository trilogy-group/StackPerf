"""Harness environment rendering service."""

from pathlib import Path
from typing import Any

from benchmark_core.config import HarnessProfileConfig, RenderFormat, Settings, VariantConfig


class RenderError(Exception):
    """Error during harness environment rendering."""


class HarnessRenderer:
    """Renders harness-specific environment snippets."""

    def __init__(self, settings: Settings | None = None):
        self.settings = settings or Settings()

    def render_environment(
        self,
        harness_profile: HarnessProfileConfig,
        variant: VariantConfig,
        api_key: str,
        base_url: str | None = None,
        model_alias: str | None = None,
        format: RenderFormat | None = None,
        extra_overrides: dict[str, str] | None = None,
    ) -> str:
        """Render environment snippet for a harness profile.

        Args:
            harness_profile: The harness profile configuration
            variant: The variant configuration
            api_key: The session API key
            base_url: Override base URL (defaults to settings)
            model_alias: Override model alias (defaults to variant)
            format: Output format (defaults to profile setting)
            extra_overrides: Additional env variable overrides

        Returns:
            Rendered environment snippet string
        """
        # Resolve values
        proxy_base = base_url or self.settings.litellm_base_url
        model = model_alias or variant.model_alias
        output_format = format or harness_profile.render_format

        # Build environment mapping
        env_vars: dict[str, str] = {
            harness_profile.base_url_env: proxy_base,
            harness_profile.api_key_env: api_key,
            harness_profile.model_env: model,
        }

        # Add extra environment from profile
        for key, template in harness_profile.extra_env.items():
            env_vars[key] = self._render_template(template, model_alias=model, **variant.benchmark_tags)

        # Add variant overrides deterministically (sorted for ordering)
        for key, value in sorted(variant.harness_env_overrides.items()):
            env_vars[key] = self._render_template(value, model_alias=model, **variant.benchmark_tags)

        # Add extra overrides (highest priority)
        if extra_overrides:
            for key, value in sorted(extra_overrides.items()):
                env_vars[key] = self._render_template(value, model_alias=model, **variant.benchmark_tags)

        # Render in requested format
        if output_format == RenderFormat.SHELL:
            return self._render_shell(env_vars)
        elif output_format == RenderFormat.DOTENV:
            return self._render_dotenv(env_vars)
        elif output_format == RenderFormat.JSON:
            return self._render_json(env_vars)
        else:
            raise RenderError(f"Unsupported render format: {output_format}")

    def _render_template(self, template: str, **context: Any) -> str:
        """Render a simple template with context variables.

        Supports {{ variable }} style templating.

        Args:
            template: Template string
            **context: Variable values

        Returns:
            Rendered string
        """
        result = template
        for key, value in context.items():
            placeholder = "{{ " + key + " }}"
            result = result.replace(placeholder, str(value))
            # Also support no-space variant
            placeholder_nospace = "{{" + key + "}}"
            result = result.replace(placeholder_nospace, str(value))
        return result

    def _render_shell(self, env_vars: dict[str, str]) -> str:
        """Render as shell export commands."""
        lines: list[str] = []
        lines.append("# Harness environment snippet")
        lines.append("# Generated by StackPerf benchmark session")
        lines.append("# WARNING: This file contains secrets - do not commit!")
        lines.append("")
        for key, value in env_vars.items():
            # Escape single quotes in values
            escaped = value.replace("'", "'\\''")
            lines.append(f"export {key}='{escaped}'")
        lines.append("")
        return "\n".join(lines)

    def _render_dotenv(self, env_vars: dict[str, str]) -> str:
        """Render as dotenv file format."""
        lines: list[str] = []
        lines.append("# Harness environment file")
        lines.append("# Generated by StackPerf benchmark session")
        lines.append("# WARNING: This file contains secrets - do not commit!")
        lines.append("")
        for key, value in env_vars.items():
            # Escape double quotes in values
            escaped = value.replace('"', '\\"')
            # Handle newlines
            escaped = escaped.replace("\n", "\\n")
            lines.append(f'{key}="{escaped}"')
        lines.append("")
        return "\n".join(lines)

    def _render_json(self, env_vars: dict[str, str]) -> str:
        """Render as JSON object."""
        import json

        lines: list[str] = []
        lines.append("{")
        lines.append('  "_comment": "Harness environment - Generated by StackPerf benchmark session",')
        lines.append('  "_warning": "This file contains secrets - do not commit!",')

        items = list(env_vars.items())
        for i, (key, value) in enumerate(items):
            comma = "," if i < len(items) - 1 else ""
            escaped = value.replace("\\", "\\\\").replace('"', '\\"')
            lines.append(f'  "{key}": "{escaped}"{comma}')

        lines.append("}")
        return "\n".join(lines)

    def render_to_file(
        self,
        harness_profile: HarnessProfileConfig,
        variant: VariantConfig,
        api_key: str,
        output_path: Path,
        base_url: str | None = None,
        model_alias: str | None = None,
        format: RenderFormat | None = None,
        extra_overrides: dict[str, str] | None = None,
    ) -> Path:
        """Render environment snippet to a file.

        The output path should be in an ignored location (not tracked by git).

        Args:
            harness_profile: The harness profile configuration
            variant: The variant configuration
            api_key: The session API key
            output_path: Path to write output file
            base_url: Override base URL
            model_alias: Override model alias
            format: Output format
            extra_overrides: Additional env variable overrides

        Returns:
            Path to the written file
        """
        content = self.render_environment(
            harness_profile=harness_profile,
            variant=variant,
            api_key=api_key,
            base_url=base_url,
            model_alias=model_alias,
            format=format,
            extra_overrides=extra_overrides,
        )

        # Ensure directory exists
        output_path.parent.mkdir(parents=True, exist_ok=True)

        # Write file
        output_path.write_text(content)

        return output_path
